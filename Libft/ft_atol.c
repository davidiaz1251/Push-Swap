#include "libft.h"

long	ft_atol(const char *str)
{
	long	i[4];

	i[0] = 0;
	i[1] = 0;
	i[2] = 1;
	i[3] = 0;
	while ((str[i[0]] >= 9 && str[i[0]] <= 13) || str[i[0]] == 32)
		i[0]++;
	while (str[i[0]] == '+' || str[i[0]] == '-')
	{
		if (i[1] >= 1)
			return (0);
		if (str[i[0]] == '-')
			i[2] = -1;
		i[1]++;
		i[0]++;
	}
	while (str[i[0]] && (str[i[0]] >= '0' && str[i[0]] <= '9'))
	{
		i[3] = i[3] * 10 + (str[i[0]] - '0');
		i[0]++;
	}
	return (i[2] * i[3]);
}
